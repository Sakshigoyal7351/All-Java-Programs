package Operators_programs;

public class bitwise_operators {

	public static void main(String[] args) {
		int a= 15;
		int b=7;
		
		// bitwise OR 
		
		System.out.println("a|b="+(a|b));
		System.out.println("a&b="+(a&b));
		System.out.println("a^b="+(a^b));
		System.out.println("~(a)"+(~a));

	}

}
